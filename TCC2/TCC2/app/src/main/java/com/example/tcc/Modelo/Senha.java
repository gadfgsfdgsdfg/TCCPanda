package com.example.tcc.Modelo;



public class Senha {

    private int senha;

    public int getSenha() {
        return senha;
    }

    public void setSenha(int senha) {
        this.senha = senha;
    }

    @Override
    public String toString() {
        return "Produto{" +
                "senha=" + senha +
                '}';
    }

}